#include<stdio.h>
int main()
{
    int a=12,b=15;
    printf("\n value of a&b is=%d",a&b);
    printf("\n value of a|b is=%d",a|b);
    printf("\n value of ~a is=%d",~a);
    printf("\n value of ~b is=%d",~b);
    printf("\n value of <<a is=%d",a<<1);
    printf("\n value of b>> is=%d",b>>1);
    printf("\n value of a^b is=%d",a^b);
}